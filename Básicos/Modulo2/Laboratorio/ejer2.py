# ¿Qué pasa al invocar la función vacía?
#  La llamamos vacía porque no hemos agregado
#  ningún argumento a la función.

print("La Witsi Witsi Araña subió a su telaraña.")
print()
print("Vino la lluvia y se la llevó.")

#  la invocación de print() vacía no esta tan vacía como se
#  esperaba - genera una línea vacía (esta interpretación
#  también es correcta) su salida es solo una nueva línea.
#  Esta no es la única forma de producir una nueva linea
#  en la consola de salida.
